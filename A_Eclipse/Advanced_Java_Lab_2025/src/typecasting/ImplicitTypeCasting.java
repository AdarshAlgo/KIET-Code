package typecasting;

public class ImplicitTypeCasting {
	public static void main(String[] args) {
		
		int i = 100;
		double j;
		j=i;
		System.out.println(j);
		
		char a ='A';
		int b =a;
		System.out.println(b);

		char c ='a';
		int d =c;
		System.out.println(d);
		
		char e ='0';
		int f =e;
		System.out.println(f);
	}
}
